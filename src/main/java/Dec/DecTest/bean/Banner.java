package Dec.DecTest.bean;

public class Banner {
      private  int id;
      private String[] mimes;
      private int w;
      private int h;
      private String ext;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String[] getMimes() {
        return mimes;
    }

    public void setMimes(String[] mimes) {
        this.mimes = mimes;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }
}
